package com.nit.Arrays;

public class Employees1 {

	int id;
	String name;
	double salary;
	
	public Employees1(int id,String name,double salary) {
		super();
		this.id=id;
		this.name=name;
		this.salary=salary;
	}

	@Override
	public String toString() {
		return "Employees1 [id=" + id + ", name=" + name + ", salary=" + salary + "]";
	}
	

}
